    public void clickOK() {
        ArrayList<PhieuDKMuonTra> listDK = new ArrayList<>();
        PhieuDKMuonTra dkmt = new PhieuDKMuonTra();

        dkmt.setMaMT(txtMaMT.getText());
        dkmt.setUserID(Integer.parseInt(txtUserID.getText()));
        dkmt.setAdminID(Integer.parseInt(txtAdminID.getText()));
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            dkmt.setNgayMuon(sdf.parse(txtNgayMuon.getText()));
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

        if (new MuonTraDAO().addDangKyMT(dkmt)) {
            JOptionPane.showMessageDialog(rootPane, "Successful!");
            userID = txtUserID.getText();
            adminID = txtAdminID.getText();
            maMT = txtMaMT.getText();
            strNgayMuon = txtNgayMuon.getText();
            adminName = txtAdmName.getText();
            userName = txtUserName.getText();
            strTienCoc = txtTienCoc.getText();
            tienCoc = Integer.valueOf(strTienCoc);
            listDK.add(dkmt);
            new ChiTietMuonTraADMIN().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "ERROR! Nhầm ID, hoặc MaMT đã Tồn Tại,...");
        }

    }

    public static String getNameAdmin(String adminID) {
        String adName = null;
        try {
            Connection con = getConnection();
            System.out.println("connect Success!");

            String query = "SELECT * FROM tbladmin WHERE AdminID = ?";
            PreparedStatement ps = con.prepareStatement(query);

            ps.setString(1, adminID);

            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                adName = rs.getString(2);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        return adName;
    }

    public String getNameUser() {
        String usrName = null;
        try {
            Connection con = getConnection();
            System.out.println("connect Success!");

            String query = "SELECT * FROM tbluser WHERE UserID = ?";
            PreparedStatement ps = con.prepareStatement(query);

            ps.setString(1, txtUserID.getText());

            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                usrName = rs.getString(2);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        return usrName;
    }

