    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() == btnCancel) {
            new AdminView().setVisible(true);
            this.dispose();
        } else if (e.getSource() == btnOK) {
            clickOK();
        } else if (e.getSource() == btnReset) {
            clickReset();
        }
    }
